import fs from 'fs';
import path from 'path';
import matter from 'gray-matter';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const lessonsDir = path.join(__dirname, '../src/content/lesson');
const redirectsFile = path.join(__dirname, '../public/_redirects');

let redirectsContent = '# Auto-generated by scripts/generate-redirects.js\n\n';

// Read all course subdirectories
const courses = fs.readdirSync(lessonsDir).filter(file => {
  return fs.statSync(path.join(lessonsDir, file)).isDirectory();
});

let premiumCourses = [];

courses.forEach(courseSlug => {
  const coursePath = path.join(lessonsDir, courseSlug);
  const lessons = fs.readdirSync(coursePath).filter(file => file.endsWith('.md'));
  
  lessons.forEach(lessonFile => {
    const lessonSlug = lessonFile.replace('.md', '');
    const lessonPath = path.join(coursePath, lessonFile);
    
    // Read frontmatter
    const fileContent = fs.readFileSync(lessonPath, 'utf-8');
    const { data } = matter(fileContent);
    
    if (!data.isFree) {
      if (process.env.UNLOCK_ALL !== 'true') {
        premiumCourses.push(`/courses/${courseSlug}/${lessonSlug}  /courses/${courseSlug}/${lessonSlug}  200! Role=course:${courseSlug}`);
        premiumCourses.push(`/courses/${courseSlug}/${lessonSlug}/  /courses/${courseSlug}/${lessonSlug}/  200! Role=course:${courseSlug}`);
        
        premiumCourses.push(`/courses/${courseSlug}/${lessonSlug}  /access-denied?returnTo=/courses/${courseSlug}/${lessonSlug}  302!`);
        premiumCourses.push(`/courses/${courseSlug}/${lessonSlug}/  /access-denied?returnTo=/courses/${courseSlug}/${lessonSlug}  302!`);
      }
    }
  });
});

if (premiumCourses.length > 0) {
  redirectsContent += '# Protect premium courses\n';
  redirectsContent += premiumCourses.join('\n') + '\n';
}

fs.writeFileSync(redirectsFile, redirectsContent);

console.log(`✅ Successfully generated _redirects for ${courses.length} courses.`);
